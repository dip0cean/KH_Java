package OOP.Static_0;

public class Robot {
//	Field	
	private int a; // 첫번째 값
	private int b; // 두번째 값
	private int c; // 결과 값

//	public Robot() {} > 클래스를 만들 때 자동 생성되는 "기본 생성자"	

	public void setA(int a) {
		this.a = a;
	}

	public int getA() {
		return this.a;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getB() {
		return this.b;
	}

	public void setC(int c) {
		this.c = c;
	}

	public int getC() {
		return this.c;
	}
}
